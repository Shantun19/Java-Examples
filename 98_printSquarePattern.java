import java.util.*;

public class Sample {

    public static void main(String[] args) {
        // pattern printing.
        int r = 5;
        int c = 5;

        for(int i=0; i<r; i++) {
            for(int j=0; j<c; j++) {
                System.out.print("*");
            }
            System.out.println();
        }


    }
}
