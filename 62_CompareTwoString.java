class HelloWorld {
    public static void main(String[] args) {
        // Java Program to Compare Strings
        String style = "Bold";
        String style2 = "Bold";
        
        if(style == style2) System.out.println("Equal");
        else System.out.println("Not Equal");
        
        if(style.equals(style2)) System.out.println("String are Equals");
        else System.out.println("String are not Equals");
    }
}
